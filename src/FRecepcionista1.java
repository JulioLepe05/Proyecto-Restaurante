    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Boton de registrar
        String clienteid = null;
        String nombre = tfNombre.getText();
        String telefono = tfTelefono.getText();

        Recepcion cc = new Recepcion(clienteid, nombre, telefono);
        if (nombre.isEmpty() || telefono.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor llene todos los campos solicitados");
        } else {

            if (cc.insertar(cnx) == 1) {
                cnx.entablar("SELECT * FROM clientes", tRegistros, 20, 40, 40);
            } else {
                JOptionPane.showMessageDialog(this, "No se pudo registrar al Propietario");
            }
        }


    }//GEN-LAST:event_jButton1ActionPerformed

