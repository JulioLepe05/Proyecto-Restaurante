     void setMensaje(String linea) {
        String menuid = linea;
        String nombre = cnx.obtenerValor("SELECT nombre FROM Alimentos WHERE menuid = '"+linea+"'").toString();
        String precio = cnx.obtenerDato("SELECT precio FROM alimentos WHERE menuid = '"+linea+"'").toString();
        String tipo = cnx.obtenerDato("SELECT tipo FROM alimentos WHERE menuid = '"+linea+"'").toString();
        Foto = (ImageIcon) cnx.obtenerValor("SELECT foto FROM alimentos WHERE menuid = '"+linea+"'");
        String cantidad = cnx.obtenerDato("SELECT cantidad FROM alimentos WHERE menuid = '"+linea+"'").toString();
        
        LId.setText(menuid);
        tNombre.setText(nombre);
        tPrecio.setText(precio);
        tTipo.setText(tipo);
        lFoto.setIcon(new ImageIcon(cnx.escalarImagen(Foto.getImage(), 299, 149)));
        tCantidad.setText(cantidad);


    }//GEN-LAST:event_jButton3ActionPerformed
    
