    private void jTable1MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MousePressed
        // SELECCIONAR COSO DE LA TABLA
        int ren = jTable1.getSelectedRow();
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
      
        if (ren >= 0) {
            int id = Integer.parseInt(modelo.getValueAt(ren, 0).toString());
            LId.setText(modelo.getValueAt(ren, 0).toString());
            tNombre.setText(modelo.getValueAt(ren, 1).toString());
            tPrecio.setText(modelo.getValueAt(ren, 2).toString());
            tCantidad.setText(modelo.getValueAt(ren, 3).toString());
            ImageIcon foto = (ImageIcon) cnx.obtenerValor("SELECT foto FROM alimentos WHERE menuid = '"+id+"'");
        lFoto.setIcon(new ImageIcon(cnx.escalarImagen(foto.getImage(), 299, 149)));
            
    }//GEN-LAST:event_jTable1MousePressed
    }
    private void bAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAgregarActionPerformed
        //AGREGAR
        String menuid = null;
        String nombre = tNombre.getText();
        String precio = tPrecio.getText();
        ImageIcon foto = Foto;
        String tipos = tTipo.getText();
        String cantidad = tCantidad.getText();
        
        Alimentos ali = new Alimentos(menuid, nombre, precio, foto, tipos, cantidad);
        if (ali.insertar(cnx) == 1) {
            cnx.entablar(menu + tipo ,jTable1, 10,30,30,30);
        } else {
            JOptionPane.showMessageDialog(this, "fallo en la insercion de datos");
        }    
    }//GEN-LAST:event_bAgregarActionPerformed

